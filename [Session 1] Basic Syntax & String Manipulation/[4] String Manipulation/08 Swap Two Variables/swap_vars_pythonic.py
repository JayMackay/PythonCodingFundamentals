# Declare and initialise two variables
first = 23
second = 45

# Confirm their values
print("Before")
print(f"first is  {first}")
print(f"second is {second}")

# Swap the values of the two variables
first, second = second, first

# Confirm the swap
print("After")
print(f"first is  {first}")
print(f"second is {second}")
