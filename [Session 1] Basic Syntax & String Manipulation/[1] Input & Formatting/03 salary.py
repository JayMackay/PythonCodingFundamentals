salary = 200
print(salary)


# Variable can appear on left or right side of assignment operator
salary = salary + 50
print(salary)

salary = salary + salary
print(salary)

# Shortcut operators

salary = 200

salary += 50  # same as: salary = salary + 50
print(salary)

salary *= 10  # same as: salary = salary * 10
print(salary)
